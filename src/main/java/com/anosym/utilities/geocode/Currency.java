    public static Currency fromIsoCode(final String isoCode) {
        for (Currency cc : values()) {
            if (cc.isoCode.equalsIgnoreCase(isoCode)) {
                return cc;
            }
        }
        throw new IllegalArgumentException("No currency defined with the specified currency code");
    }

    public static Currency fromName(final String name) {
        return valueOf(name.toUpperCase());
    }
